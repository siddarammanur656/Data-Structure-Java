public class Pattern1{
    
    public static void main(String [] args ){
        
        int n=5;
        
        for(int i=n; i>=1;i--){
            for(int j=i; j<=n; j++){
                System.out.print(j);
            }
            
            for(int k=i; k>1; k--){
                    System.out.print(5);
                }
            
            System.out.println();
        }
        
    }
    
}